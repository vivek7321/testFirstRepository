	/**
     * @see ServletContextListener#contextInitialized(ServletContextEvent)
     */
    public void contextInitialized(ServletContextEvent event)  { 
    	
         // TODO Auto-generated method stub
    	
      System.out.println("Scheduler Started");

		SchedulerMail mailStart = new SchedulerMail();
		try
		{
		System.out.println("inside try block");
		mailStart.callScheduler();
		}
		catch(InterruptedException ex)
		{
			System.out.println("inside catch-->"+ex);
		}
      	
   // initialize log4j here
      ServletContext context = event.getServletContext();
      
      System.setProperty("rootPath", context.getRealPath("/"));
      System.out.println( context.getRealPath("/"));
      
      String log4jConfigFile = context.getInitParameter("log4j-config-location");
      String fullPath = context.getRealPath("") + File.separator + log4jConfigFile;
      
      System.out.println("startup listener full path --> " + fullPath);
      
//     PropertyConfigurator.configure(fullPath);
    // DOMConfigurator.configure(fullPath);
      
      String rootPath = context.getRealPath("/");
      LoggerContext logcontext = (LoggerContext) LoggerFactory.getILoggerFactory();
      JoranConfigurator jc = new JoranConfigurator();
      jc.setContext(logcontext);
      logcontext.reset(); // override default configuration
      // inject the name of the current application as "application-name"
      // property of the LoggerContext
      logcontext.putProperty("rootPath", rootPath);
      try {
		jc.doConfigure(fullPath);
		} catch (JoranException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		} 
       
    }

