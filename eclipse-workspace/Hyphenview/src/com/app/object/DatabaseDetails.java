package com.app.object;

public class DatabaseDetails {
	public String dbDetailsId;
	public String rdbms;
	public String domainName;
	public String portNumber;
	public String schemaName;
	public String userName;
	public String password;
	public String customerId;
	/**
	 * @return the dbDetailsId
	 */
	public String getDbDetailsId() {
		return dbDetailsId;
	}
	/**
	 * @param dbDetailsId the dbDetailsId to set
	 */
	public void setDbDetailsId(String dbDetailsId) {
		this.dbDetailsId = dbDetailsId;
	}
	/**
	 * @return the rdbms
	 */
	public String getRdbms() {
		return rdbms;
	}
	/**
	 * @param rdbms the rdbms to set
	 */
	public void setRdbms(String rdbms) {
		this.rdbms = rdbms;
	}
	/**
	 * @return the domainName
	 */
	public String getDomainName() {
		return domainName;
	}
	/**
	 * @param domainName the domainName to set
	 */
	public void setDomainName(String domainName) {
		this.domainName = domainName;
	}
	/**
	 * @return the portNumber
	 */
	public String getPortNumber() {
		return portNumber;
	}
	/**
	 * @param portNumber the portNumber to set
	 */
	public void setPortNumber(String portNumber) {
		this.portNumber = portNumber;
	}
	/**
	 * @return the schemaName
	 */
	public String getSchemaName() {
		return schemaName;
	}
	/**
	 * @param schemaName the schemaName to set
	 */
	public void setSchemaName(String schemaName) {
		this.schemaName = schemaName;
	}
	/**
	 * @return the userName
	 */
	public String getUserName() {
		return userName;
	}
	/**
	 * @param userName the userName to set
	 */
	public void setUserName(String userName) {
		this.userName = userName;
	}
	/**
	 * @return the password
	 */
	public String getPassword() {
		return password;
	}
	/**
	 * @param password the password to set
	 */
	public void setPassword(String password) {
		this.password = password;
	}
	/**
	 * @return the customerId
	 */
	public String getCustomerId() {
		return customerId;
	}
	/**
	 * @param customerId the customerId to set
	 */
	public void setCustomerId(String customerId) {
		this.customerId = customerId;
	}
	
}
