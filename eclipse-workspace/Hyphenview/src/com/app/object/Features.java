package com.app.object;

public class Features {
	
	public String featureId;
	public String featureName;
	public String featureDescription;
	public int featureCost;
	
	/**
	 * @return the featureId
	 */
	public String getFeatureId() {
		return featureId;
	}
	/**
	 * @param featureId the featureId to set
	 */
	public void setFeatureId(String featureId) {
		this.featureId = featureId;
	}
	/**
	 * @return the featureName
	 */
	public String getFeatureName() {
		return featureName;
	}
	/**
	 * @param featureName the featureName to set
	 */
	public void setFeatureName(String featureName) {
		this.featureName = featureName;
	}
	/**
	 * @return the featureDescription
	 */
	public String getFeatureDescription() {
		return featureDescription;
	}
	/**
	 * @param featureDescription the featureDescription to set
	 */
	public void setFeatureDescription(String featureDescription) {
		this.featureDescription = featureDescription;
	}
	/**
	 * @return the featureCost
	 */
	public int getFeatureCost() {
		return featureCost;
	}
	/**
	 * @param featureCost the featureCost to set
	 */
	public void setFeatureCost(int featureCost) {
		this.featureCost = featureCost;
	}
	
	
}
